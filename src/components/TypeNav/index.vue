<template>
  <div class="type-nav">
    <div class="container">
      <div @mouseleave="leaveShow" class="container" @mouseenter="enterShow">
        <h2 class="all">全部商品分类</h2>
        <nav class="nav">
          <a href="###">服装城</a>
          <a href="###">美妆馆</a>
          <a href="###">尚品汇超市</a>
          <a href="###">全球购</a>
          <a href="###">闪购</a>
          <a href="###">团购</a>
          <a href="###">有趣</a>
          <a href="###">秒杀</a>
        </nav>
        <!-- 添加过渡动画 -->
        <transition name="sort">
          <div class="sort" v-show="show">
            <div class="all-sort-list2" @click="goSearch">
              <!-- 一级分类 -->
              <div
                class="item bo"
                v-for="(c1, index) in categoryList"
                :key="c1.categoryId"
                :class="{ cur: currentIndex == index }"
              >
                <h3 @mouseenter="changeIndex(index)">
                  <!-- 给a标签添加属性来区分 -->
                  <a
                    :data-categoryName="c1.categoryName"
                    :data-category1Id="c1.categoryId"
                    >{{ c1.categoryName }}</a
                  >
                </h3>
                <!-- 二级分类 -->
                <div
                  class="item-list clearfix"
                  :style="{ display: currentIndex == index ? 'block' : 'none' }"
                >
                  <div
                    class="subitem"
                    v-for="c2 in c1.categoryChild"
                    :key="c2.categoryId"
                  >
                    <dl class="fore">
                      <dt>
                        <a
                          :data-categoryName="c2.categoryName"
                          :data-category2Id="c2.categoryId"
                          >{{ c2.categoryName }}</a
                        >
                      </dt>
                      <dd>
                        <!-- 三级分类 -->
                        <em v-for="c3 in c2.categoryChild" :key="c3.categoryId">
                          <a
                            :data-categoryName="c3.categoryName"
                            :data-category3Id="c3.categoryId"
                            >{{ c3.categoryName }}</a
                          >
                        </em>
                      </dd>
                    </dl>
                  </div>
                </div>
              </div>
            </div>
          </div>
        </transition>
      </div>
    </div>
  </div>
</template>

<script>
import { mapState } from "vuex";
import throttle from "lodash/throttle";
export default {
  name: "TypeNav",
  data() {
    return {
      currentIndex: -1,
      show: true,
    };
  },
  mounted() {
    // 如果不是home组件,就将typeNav设置为隐藏
    // 挂在完毕就将show改为false
    if (this.$route.path != "/home") {
      this.show = false;
    }
  },
  computed: {
    ...mapState({
      // 注入一个参数state，其实为大仓库中的数据
      categoryList: (state) => state.home.categoryList,
    }),
  },
  methods: {
    // 鼠标进入修改响应式数据currentIndex属性,使用节流的方式防止出现卡顿现象.
    // changeIndex(index){
    // },
    changeIndex: throttle(function (index) {
      this.currentIndex = index;
    }, 50),
    leaveIndex() {
      this.currentIndex = -1;
    },
    // 使用编程式导航+事件的委派来解决路由传参
    goSearch(event) {
      // 获取到点击事件
      let element = event.target;
      // 从element上获取到属性数据集
      let { categoryname, category1id, category2id, category3id } =
        element.dataset;
      // 如果标签身上属性由categoryname属性,则就是a标签
      if (categoryname) {
        // 整理路由参数
        let location = { name: "search" };
        let query = { categoryName: categoryname };
        if (category1id) {
          query.category1Id = category1id;
        } else if (category2id) {
          query.category2Id = category2id;
        } else {
          query.category3Id = category3id;
        }
        // 判断路由跳转时,带有params参数,也要一起带走
        if (this.$route.params) {
          location.params = this.$route.params;
          // 整理参数
          location.query = query;
          this.$router.push(location);
        }
      }
    },
    // 当鼠标移入时展示
    enterShow() {
      this.show = true;
    },
    // 当鼠标离开时隐藏
    leaveShow() {
      this.currentIndex = -1;
      // 判断search路由时,我们才隐藏导航
      if (this.$route.path != "/home") {
        this.show = false;
      }
    },
  },
};
</script>

